package a1229;

public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
        // Java 실행되면 제일 먼저 main 메서드가 실행
        // Public 접근 제어자 - 외부에서 접근 할 수 있는 제약
        // private -> protected -> public
        // static 정적 메서드 - 객체 생성 없이 클래스 이름으로 바로 접근 가능
        // 공통으로 하나만 존재
        // 메모리에 한 번만 생성
        // void 반환값 없음
    }
}
